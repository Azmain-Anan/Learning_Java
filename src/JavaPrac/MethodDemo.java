package JavaPrac;

public class MethodDemo {

	public static void main(String[] args) {
		MethodDemo mt = new MethodDemo();
		mt.logIn();
		mt.logOut();
		
	}

	public void logIn()
	{
		System.out.println("Successfully LogIn");
	}
	
	public void logOut()
	{
		System.out.println("Successfully LogOut");
	}
}
